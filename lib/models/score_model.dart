class ScoreModel {
  final String totalPlus;
  final String totalMinus;
  final String totalScore;
  final String number;
  final String name;
  final String reason;
  final String score;
  final String date;

  const ScoreModel({
    required this.totalPlus,
    required this.totalMinus,
    required this.totalScore,
    required this.number,
    required this.name,
    required this.reason,
    required this.score,
    required this.date,
  });
}
