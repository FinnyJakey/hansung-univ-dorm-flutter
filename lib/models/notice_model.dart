class NoticeModel {
  final bool fixed;
  final String title;
  final bool attachment;
  final String name;
  final String date;
  final String count;
  final String idx;

  const NoticeModel({
    required this.fixed,
    required this.title,
    required this.attachment,
    required this.name,
    required this.date,
    required this.count,
    required this.idx,
  });
}
